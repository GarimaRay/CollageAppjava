    public void addListToTable() {
    DefaultTableModel tableModel = (DefaultTableModel) tblStudent.getModel();

    // Clear existing rows in the table
    tableModel.setRowCount(0);

    // Add each student from the list to the table
    for (StudentModel student : studentList) {
        tableModel.addRow(new Object[]{
            student.getLmuId(),
            student.getName(),
            student.getProgram(),
            student.getContact(),
            student.getAge()
        });
    }

}
 private static final String FILE_NAME = "students.txt"; // Ensure the file is in the program's working directory

// Save the studentList to a file
private void saveDataToFile() {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(FILE_NAME))) { 
        for (StudentModel student : studentList) {
            writer.write(student.getLmuId() + "," +
                         student.getName() + "," +
                         student.getProgram() + "," +
                         student.getContact() + "," +
                         student.getAge());
            writer.newLine();
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(rootPane, "Error saving data: " + e.getMessage());
    }
}

// Load the studentList from a file
private void loadDataFromFile() {
    studentList = new LinkedList<>();
    File file = new File(FILE_NAME); // Check if the file exists before reading
    if (!file.exists()) {
        System.out.println("No existing file found. Starting with an empty list.");
        return;
    }

    try (BufferedReader reader = new BufferedReader(new FileReader(FILE_NAME))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length == 5) {
                int lmuId = Integer.parseInt(parts[0]);
                String name = parts[1];
                String program = parts[2];
                String contact = parts[3];
                short age = Short.parseShort(parts[4]);
                studentList.add(new StudentModel(lmuId, name, program, contact, age));
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(rootPane, "Error loading data: " + e.getMessage());
    }
}

// Start the application
public void startApp() {
    loadDataFromFile(); // Load existing data from the file
    addListToTable();   // Populate the table with the loaded data
}
