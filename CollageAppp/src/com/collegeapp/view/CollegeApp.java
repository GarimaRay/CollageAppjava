    @SuppressWarnings("empty-statement")
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
                                        
    int lmuId = Integer.parseInt(txtFldLmuId.getText());
    String name = jTextField2.getText();
    String program = jTextField3.getText();
    String contact = jTextField5.getText();
    short age = 0;

    // Empty validation
    if (ValidationUtil.isNullOrEmpty(txtFldLmuId.getText())) {
        JOptionPane.showMessageDialog(rootPane, "Please enter the LMU ID.");
        return;
    }
    if (ValidationUtil.isNullOrEmpty(name)) {
        JOptionPane.showMessageDialog(rootPane, "Please enter the Name.");
        return;
    }
    if (ValidationUtil.isNullOrEmpty(program)) {
        JOptionPane.showMessageDialog(rootPane, "Please enter the Program.");
        return;
    }
    if (ValidationUtil.isNullOrEmpty(contact)) {
        JOptionPane.showMessageDialog(rootPane, "Please enter the Contact.");
        return;
    }
    if (ValidationUtil.isNullOrEmpty(jTextField6.getText())) {
        JOptionPane.showMessageDialog(rootPane, "Please enter the Age.");
        return;
    } else {
        age = (short) Integer.parseInt(jTextField6.getText());
    }

    // Validation checks
    if (!validationUtil.isValidLmuId(lmuId)) {
        JOptionPane.showMessageDialog(rootPane, "Invalid LMU ID.");
        return;
    }
    if (!validationUtil.isValidProgram(program.toUpperCase())) {
        JOptionPane.showMessageDialog(rootPane, "Invalid Program.");
        return;
    }
    if (!validationUtil.isValidName(name)) {
        JOptionPane.showMessageDialog(rootPane, "Invalid Name.");
        return;
    }
    if (!validationUtil.isValidAge(age)) {
        JOptionPane.showMessageDialog(rootPane, "Invalid Age.");
        return;
    }
    if (!validationUtil.isValidContact(contact)) {
        JOptionPane.showMessageDialog(rootPane, "Invalid Contact.");
        return;
    }

    // Update the student in the list
    boolean isUpdated = false;
    for (StudentModel student : studentList) {
        if (student.getLmuId() == lmuId) { // Match LMU ID
            student.setName(name);
            student.setProgram(program);
            student.setContact(contact);
            student.setAge(age);
            isUpdated = true;
            break;
            
        }
    }

    if (!isUpdated) {
        JOptionPane.showMessageDialog(rootPane, "LMU ID not found. Adding as a new entry.");
        StudentModel newStudent = new StudentModel(lmuId, name, program, contact, age);
        studentList.add(newStudent);
    }

    // Refresh the table to reflect updates
    addListToTable();

    // Clear fields
    clearField();
    saveDataToFile();
    JOptionPane.showMessageDialog(rootPane, "Data updated successfully!");

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                                          
    // Retrieve input data
    int lmuId = Integer.parseInt(txtFldLmuId.getText());
    String name = jTextField2.getText();
    String program = jTextField3.getText();
    String contact = jTextField5.getText();
    short age = 0;

    // Empty field validation
    if (ValidationUtil.isNullOrEmpty(txtFldLmuId.getText()) ||
        ValidationUtil.isNullOrEmpty(name) ||
        ValidationUtil.isNullOrEmpty(program) ||
        ValidationUtil.isNullOrEmpty(contact) ||
        ValidationUtil.isNullOrEmpty(jTextField6.getText())) {
        JOptionPane.showMessageDialog(rootPane, "Please fill in all the fields.");
        return;
    }

    // Parse and validate age
    age = (short) Integer.parseInt(jTextField6.getText());
    if (!validationUtil.isValidAge(age)) {
        JOptionPane.showMessageDialog(rootPane, "Invalid age. Please enter a valid age.");
        return;
    }

    // Find the student in the list using lmuId
    boolean found = false;
    for (StudentModel student : studentList) {
        if (student.getLmuId() == lmuId) {
            // Update student details
            student.setName(name);
            student.setProgram(program);
            student.setContact(contact);
            student.setAge(age);
            found = true;
            break;
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(rootPane, "Student with LMU ID " + lmuId + " not found.");
        return;
    }

    // Update the table to reflect the changes
    addListToTable();

    // Clear input fields
    clearField();
    saveDataToFile();

    // Show success message
    JOptionPane.showMessageDialog(rootPane, "Student details updated successfully.");
                                         

        // method call
    }//GEN-LAST:event_btnUpdateActionPerformed
       
